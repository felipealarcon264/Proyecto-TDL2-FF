package modelo.enums;

/**
 * Enumeración que representa los géneros de contenido audiovisual.
 * Se utiliza para clasificar el contenido y para las preferencias de los
 * perfiles.
 *
 * @author Grupo 4 - Proyecto TDL2
 * @version 1.0
 * 
 */
public enum Genero {
    /**
     * Género de acción, centrado en secuencias de combate, persecuciones y
     * aventura.
     */
    ACCION,

    /**
     * Género de animación japonesa.
     */
    ANIME,

    /**
     * Género centrado en el desarrollo de personajes y conflictos emocionales.
     */
    DRAMA,

    /**
     * Género diseñado para provocar humor y risa.
     */
    COMEDIA,

    /**
     * Género que busca provocar miedo, suspenso y horror.
     */
    TERROR
}
